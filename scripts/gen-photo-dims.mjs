#!/usr/bin/env node
// Generate src/data/photoDims.js — a map of '/photos/<file>' → 'w/h' aspect ratio.
// The gallery masonry uses these to reserve each image's final box before it
// loads, so the layout never reflows as photos arrive. Dimensions come from the
// originals (derivatives keep the same ratio). macOS `sips`, no deps.
// Re-run after adding photos (gen-photo-derivatives.sh runs this for you).
import { execFileSync } from 'node:child_process';
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

// EXIF orientation values 5–8 mean the image is stored rotated ±90° and the
// browser displays it with width/height swapped. sips reports only the stored
// pixel dims, so we parse the JPEG's EXIF orientation tag (0x0112) ourselves.
function isTransposed(path) {
  if (!/\.jpe?g$/i.test(path)) return false;
  const buf = readFileSync(path);
  if (buf.readUInt16BE(0) !== 0xffd8) return false;
  let off = 2;
  while (off + 4 <= buf.length) {
    if (buf[off] !== 0xff) break;
    const marker = buf[off + 1];
    if (marker === 0xda) break; // start of scan — no EXIF past here
    const size = buf.readUInt16BE(off + 2);
    if (marker === 0xe1 && buf.toString('ascii', off + 4, off + 10) === 'Exif\0\0') {
      const tiff = off + 10;
      const le = buf.toString('ascii', tiff, tiff + 2) === 'II';
      const read16 = (p) => (le ? buf.readUInt16LE(p) : buf.readUInt16BE(p));
      const read32 = (p) => (le ? buf.readUInt32LE(p) : buf.readUInt32BE(p));
      const ifd = tiff + read32(tiff + 4);
      const count = read16(ifd);
      for (let i = 0; i < count; i++) {
        const entry = ifd + 2 + i * 12;
        if (read16(entry) === 0x0112) return read16(entry + 8) >= 5;
      }
      return false;
    }
    off += 2 + size;
  }
  return false;
}

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const photosDir = join(root, 'photos');
const IMG_RE = /\.(jpe?g|png)$/i;
const SKIP_DIRS = new Set(['thumbs', 'web']);

function* walk(dir, rel = '') {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (!SKIP_DIRS.has(entry.name)) yield* walk(join(dir, entry.name), `${rel}${entry.name}/`);
    } else if (IMG_RE.test(entry.name)) {
      yield { path: join(dir, entry.name), key: `/photos/${rel}${entry.name}` };
    }
  }
}

const dims = {};
let swapped = 0;
for (const { path, key } of walk(photosDir)) {
  const out = execFileSync('sips', ['-g', 'pixelWidth', '-g', 'pixelHeight', path], { encoding: 'utf8' });
  let w = Number(out.match(/pixelWidth: (\d+)/)?.[1]);
  let h = Number(out.match(/pixelHeight: (\d+)/)?.[1]);
  if (isTransposed(path)) { [w, h] = [h, w]; swapped++; }
  if (w && h) dims[key] = `${w}/${h}`;
}

const entries = Object.keys(dims).sort().map((k) => `  '${k}': '${dims[k]}',`).join('\n');
const banner = '// Generated by scripts/gen-photo-dims.mjs — do not edit by hand.\n'
  + '// \'/photos/<file>\' → \'width/height\' of the original (derivatives share the ratio).\n';
writeFileSync(join(root, 'src/data/photoDims.js'), `${banner}export const PHOTO_RATIOS = {\n${entries}\n};\n`);
console.log(`Wrote ${Object.keys(dims).length} ratios to src/data/photoDims.js (${swapped} EXIF-rotated)`);
